package com.protectdev.manage.pojo;

public class SZone {

    private int id;
    private String sZoneName;
    private int temId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getsZoneName() {
        return sZoneName;
    }

    public void setsZoneName(String sZoneName) {
        this.sZoneName = sZoneName;
    }

    public int getTemId() {
        return temId;
    }

    public void setTemId(int temId) {
        this.temId = temId;
    }
}
