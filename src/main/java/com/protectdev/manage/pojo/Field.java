package com.protectdev.manage.pojo;

public class Field {

    private int id;
    private String fieldName;
    private int temId;
    private int sZoneId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFieldName() {
        return fieldName;
    }

    public void setFieldName(String fieldName) {
        this.fieldName = fieldName;
    }

    public int getTemId() {
        return temId;
    }

    public void setTemId(int temId) {
        this.temId = temId;
    }

    public int getsZoneId() {
        return sZoneId;
    }

    public void setsZoneId(int sZoneId) {
        this.sZoneId = sZoneId;
    }
}
